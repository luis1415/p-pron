    <p>Los modelos parten de datos obtenidos en radiosondeos, satélites meteorológicos, y observaciones meterológicas en tierra. Las observaciones que están distribuidas de forma irregular se procesan por asimilación de datos y métodos de análisis objetivos que realizan un control de calidad y obtienen valores utilizables por los algoritmos matemáticos de los modelos numéricos (generalmente dispuestos en una cuadrícula uniformemente espaciada). A continuación estos datos se usan en el modelo como punto de partida para la previsión. El conjunto de ecuaciones empleado se conoce como "ecuaciones primitivas".</p>
